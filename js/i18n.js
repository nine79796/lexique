const LANG_META = {
  fr: { flag: '🇫🇷', label: 'FR', dir: 'ltr' },
  en: { flag: '🇬🇧', label: 'EN', dir: 'ltr' },
  es: { flag: '🇪🇸', label: 'ES', dir: 'ltr' },
};

let currentLang = 'fr';

function detectLang() {
  const saved = localStorage.getItem(LS_KEY_LANG);
  if (saved && TRANSLATIONS[saved]) return saved;
  const nav  = (navigator.languages && navigator.languages[0]) || navigator.language || 'fr';
  const code = nav.slice(0, 2).toLowerCase();
  return TRANSLATIONS[code] ? code : 'fr';
}

/** Translate a key, with optional fallback to French then raw key. */
function t(key, fallback) {
  return (TRANSLATIONS[currentLang]?.[key]) ?? (TRANSLATIONS.fr[key]) ?? fallback ?? key;
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem(LS_KEY_LANG, lang);
  applyTranslations();
  closeLangDropdown();
  render();
  renderTaskFilters();
  renderTasks();
  renderCatManager();
  updateStats();
  updateTaskStats();
}

function applyTranslations() {
  const meta = LANG_META[currentLang];
  document.documentElement.lang = currentLang;
  document.documentElement.dir  = meta.dir;
  document.getElementById('langFlag').textContent  = meta.flag;
  document.getElementById('langLabel').textContent = meta.label;

  document.querySelectorAll('.lang-option').forEach(el => {
    el.classList.toggle('active', el.dataset.lang === currentLang);
  });
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.getAttribute('data-i18n'));
    if (val) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t(el.getAttribute('data-i18n-placeholder'));
    if (val) el.placeholder = val;
  });
  document.querySelectorAll('[data-i18n-opt]').forEach(el => {
    const val = t(el.getAttribute('data-i18n-opt'));
    if (val) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-day]').forEach(el => {
    const val = t('day.' + el.getAttribute('data-i18n-day'));
    if (val) el.textContent = val;
  });

  // FIX: theme button text is now translated via i18n keys instead of
  // being hardcoded in French regardless of the active language.
  const isLight = document.body.classList.contains('light');
  document.getElementById('themeBtn').textContent = isLight ? t('theme.dark') : t('theme.light');
}

function toggleLangDropdown() {
  document.getElementById('langDropdown').classList.toggle('open');
}

function closeLangDropdown() {
  document.getElementById('langDropdown').classList.remove('open');
}

// Close lang dropdown when clicking outside
document.addEventListener('click', e => {
  const wrap = document.getElementById('langBtnWrap');
  if (wrap && !wrap.contains(e.target)) closeLangDropdown();
});
